#!/usr/bin/env bash
# Run every benchmark and rebuild RESULTS.md.
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
BENCH_DIR="$REPO_ROOT/benchmarks"

NAMES=()
for d in "$BENCH_DIR/challenges"/*/; do
    NAMES+=("$(basename "$d")")
done

if [[ ${#NAMES[@]} -eq 0 ]]; then
    echo "no challenges found in $BENCH_DIR/challenges" >&2
    exit 1
fi

echo "running ${#NAMES[@]} benchmarks..."
for n in "${NAMES[@]}"; do
    echo ""
    echo "=== $n ==="
    bash "$BENCH_DIR/scripts/run_bench.sh" "$n" || echo "  benchmark $n failed"
done

# Rebuild RESULTS.md from the latest JSON for each benchmark
{
    echo "# Benchmark results"
    echo ""
    echo "_Auto-generated by run_all.sh. Do not hand-edit; the next run will overwrite._"
    echo ""
    echo "Last full run: $(date -u +%Y-%m-%d\ %H:%M\ UTC)"
    echo ""
    echo "| Benchmark | Solved | Duration (s) | Tokens | Commit |"
    echo "|-----------|--------|--------------|--------|--------|"
    for n in "${NAMES[@]}"; do
        latest="$(ls -t "$BENCH_DIR/results/"*"-$n.json" 2>/dev/null | head -1 || true)"
        if [[ -n "$latest" ]]; then
            solved=$(python3 -c "import json,sys;d=json.load(open('$latest'));print(d.get('solved'))" 2>/dev/null || echo "n/a")
            duration=$(python3 -c "import json;d=json.load(open('$latest'));print(d.get('duration_seconds'))" 2>/dev/null || echo "n/a")
            tokens=$(python3 -c "import json;d=json.load(open('$latest'));print(d.get('tokens_used'))" 2>/dev/null || echo "n/a")
            commit=$(python3 -c "import json;d=json.load(open('$latest'));print(d.get('commit'))" 2>/dev/null || echo "n/a")
            echo "| $n | $solved | $duration | $tokens | $commit |"
        else
            echo "| $n | n/a | n/a | n/a | n/a |"
        fi
    done
} > "$BENCH_DIR/results/RESULTS.md"

echo ""
echo "wrote $BENCH_DIR/results/RESULTS.md"
